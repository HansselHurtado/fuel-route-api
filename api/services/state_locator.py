@functools.lru_cache(maxsize=1)
def _build_tree():
    coords = np.array([(lat, lon) for lat, lon, _ in _CITIES_RAW])
    states = [s for _, _, s in _CITIES_RAW]
    tree = cKDTree(coords)
    return tree, states


def get_state_for_coordinate(lat: float, lon: float) -> str | None:
    tree, states = _build_tree()
    _, idx = tree.query([lat, lon])
    return states[idx]


def batch_lookup_states(coords: list[tuple[float, float]]) -> list[str | None]:
    """Return state abbreviation for each (lat, lon) pair. Fully offline."""
    if not coords:
        return []
    tree, states = _build_tree()
    pts = np.array(coords)
    _, indices = tree.query(pts)
    return [states[i] for i in indices]


def build_route_segments(
    route_coords: list[tuple[float, float]],
    cumulative_miles: list[float],
) -> list[dict]:
    """
    Sample the route, detect states offline, and return ordered state segments.
    Returns list of dicts with: state_abbrev, start_mile, end_mile, entry_lat, entry_lon.
    """
    if not route_coords:
        return []

    total_miles = cumulative_miles[-1]
    # Sample up to 200 points for accuracy without being too slow
    step = max(1, len(route_coords) // 200)
    sampled_indices = list(range(0, len(route_coords), step))
    if sampled_indices[-1] != len(route_coords) - 1:
        sampled_indices.append(len(route_coords) - 1)

    sampled_coords = [route_coords[i] for i in sampled_indices]
    sampled_miles = [cumulative_miles[i] for i in sampled_indices]

    state_labels = batch_lookup_states(sampled_coords)

    segments = []
    current_state = None

    for state, mile, coord in zip(state_labels, sampled_miles, sampled_coords):
        if state != current_state:
            if segments:
                segments[-1]["end_mile"] = mile
            segments.append({
                "state_abbrev": state,
                "start_mile": mile,
                "end_mile": total_miles,
                "entry_lat": coord[0],
                "entry_lon": coord[1],
            })
            current_state = state

    if segments:
        segments[-1]["end_mile"] = total_miles

    return segments
